    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        
        String easting = eastings.getText();
        String northing = northings.getText();
        
        
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        dataset.setValue(new Float (easting), "Coordinates", "Easting");
        dataset.setValue(new Double (northing), "Coordinates", "Northing");
        JFreeChart chart = ChartFactory.createBarChart("Coordinate Values of Selected Road", "Coordinates", "Values", dataset, PlotOrientation.VERTICAL, false, true, false);
        CategoryPlot p = chart.getCategoryPlot();
        p.setRangeGridlinePaint(Color.black);
        ChartFrame frame = new ChartFrame("Bar Chart", chart);
        frame.setVisible(true);
        frame.setSize(450, 350);
        
        
        
    }//GEN-LAST:event_jButton10ActionPerformed

    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        String carsandtaxis = cartaxi.getText();
        String pedalcycles = pedalcy.getText();
        
        
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        dataset.setValue(new Double (carsandtaxis), "Number of Cars, Taxis and Pedal Cycles", "Cars and Taxis");
        dataset.setValue(new Double (pedalcycles), "Number of Cars, Taxis and Pedal Cycles", "Pedal Cycles");
        
        
  
        JFreeChart chart = ChartFactory.createLineChart3D("Contribution of Cars, Taxis and Pedal Cycles in specific road", "Motorised Vehicle", "Number of Cars, Taxis and Pedal Cycles", dataset, PlotOrientation.VERTICAL, false, true, false);
        chart.setBackgroundPaint (Color.WHITE);
        chart.getTitle().setPaint(Color.BLACK); 
        CategoryPlot p = chart.getCategoryPlot();
        p.setRangeGridlinePaint(Color.BLUE);
        ChartFrame frame = new ChartFrame("Bar Chart", chart);
        frame.setVisible(true);
        frame.setSize(450, 350);
        
    }//GEN-LAST:event_jButton11ActionPerformed

