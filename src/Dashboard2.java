    private void updateTable(){
        String sql = "select local_authority_id, count_point_id, road_name, pedal_cycles, cars_and_taxis, lgvs, year, hour, easting, northing, link_length_miles from Major_Road_Local_Authority inner join Count_Point on Count_Point.cId = Major_Road_Local_Authority.cId ";
        
        try{
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            Table_MajorRoad.setModel(DbUtils.resultSetToTableModel(rs));
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        
        }

    }
            
            
