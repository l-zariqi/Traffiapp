    // Filling text fields with data from Table/DB using Mouse Click
    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        String selection = jTable1.getModel().getValueAt(row, 0).toString();
        String sql = "SELECT * FROM User_Registration where userID = " + selection ;
        try {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next()){

               txt_fname.setText(rs.getString("userFirstName"));
               txt_sname.setText(rs.getString("userSurname"));
               txt_gender.setText(rs.getString("userGender"));
               txt_email.setText(rs.getString("userEmail"));
               txt_telno.setText(rs.getString("userTel"));
               txt_uname.setText(rs.getString("userName"));
               txt_pass.setText(rs.getString("userPassword"));
               txt_type.setText(rs.getString("type"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally{
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

