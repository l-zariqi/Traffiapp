    // update jTable1 with DB
    private void updateTable() {
        String sql = "SELECT userID,userFirstName,userSurname,userGender,userEmail,userTel,userName,userPassword,type FROM User_Registration";
        try {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally{
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
            }
        }
        
    }
    
    //update comboBox
    private void updateCombo() {
        String sql = "select * from User_Registration";
        try {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                jComboBox1.addItem(rs.getString("userID"));
            }
        } catch (Exception e) {
        } finally{
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
            }
        }
    }
    
